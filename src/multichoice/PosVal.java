public class PosVal {
    
    private int pos = -1;
    private int val = -1;

    public PosVal(int pos, int val) {
        this.pos = pos;
        this.val = val;
    }

    public int getPos() {
        return pos;
    }

    public int getVal() {
        return val;
    }
    
} // PosVal
